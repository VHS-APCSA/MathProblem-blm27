public class ABC 
{
	private double a;
	private double b;
	private double c;
	public ABC()
	{
		a = 0.0;
		b = 0.0;
		c = 0.0;
	}
	public ABC(double a, double b, double c) 
	{
		this.a = a;
		this.b = b;
		this.c = c;
	}
	public double getA() 
	{
		return a;
	}
	public void setA(double a) 
	{
		this.a = a;
	}
	public double getB() 
	{
		return b;
	}
	public void setB(double b) 
	{
		this.b = b;
	}
	public double getC() 
	{
		return c;
	}
	public void setC(double c) 
	{
		this.c = c;
	}
	
}
